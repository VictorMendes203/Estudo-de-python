#Funções
#Função "abs" retorna um numero absoluto
print(abs(-15))

#Função "pow" faz a esponenciação
print(pow(3,3))

#Função "max" idenifica o numero maximo
print(max(1,5, 18))

#Função "min" identifica o numero minimo
print(min(1,8, -5))

#Função "round" faz o arendondamento para baixo ou para cima, dependendo do valor mais proximo
print(round(9.4))
print(round(5.7))

#Função "floor" arredonda pra baixo! Mas e preciso importar a biblioteca math
print(math.floor(9.4))

#Função "ceil" arredonda pra cima! Mas e preciso importar a biblioteca math
print(math.ceil(9.4))

#Função "sqrt" faz raiz quadrada! Mas e preciso importar a biblioteca math
print(math.sqrt(9))





